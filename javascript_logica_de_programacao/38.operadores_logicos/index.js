/* 
Operadores Lógicos
&& -> AND -> E
|| -> OR -> OU
! -> NOT -> NÃO
*/
console.log(true && true); // retorna true
console.log(false || false || true || false); // retorna true
console.log(false || false || false || false); // retorna false
console.log(!false); // inverte uma expressão
console.log(!!false); // podemos negar duas vezes a expressão